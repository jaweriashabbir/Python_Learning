no_1 = int(input('Enter no.1:'))
no_2 = int(input('Enter no.2:'))
command = str(input('Enter command:'))
if command == "+":
    print(no_1 + no_2)
elif command == "-":
    print(no_1 - no_2)
elif command == "*":
    print(no_1 * no_2)
elif command == "/":
    print(no_1 / no_2)
elif command == "%":
    print(no_1 % no_2)
elif command == "**":
    print(no_1 ** no_2)
